int	ft_sqrt(int nb)
{
	int	x;

	x = 0;
	if (nb == 1)
		return (1);
	if (nb <= 0)
		return (0);
	while (x <= nb / 2 && x <= 46341)
	{
		if (x * x == nb)
		{
			return (x);
		}
		else
			x++;
	}
	return (0);
}
