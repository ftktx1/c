int	ft_any(char **tab, int (*f)(char*))
{
	int counter;
	int i;

	counter = 0;
	i = 0;
	while (tab[counter] != '\0')
	{
		if ((*f)(tab[counter]) == 1)
			i = 1;
		counter++;
	}
	return (i);
}
